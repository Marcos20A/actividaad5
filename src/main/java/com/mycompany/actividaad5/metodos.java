public class metodos {
    
    public int suma (int num1 , int num2){
        int resultS = 0;
        resultS = num1 + num2; 
        return resultS;
    }
    
    public int resta (int num1 , int num2){
        int resultR = 0;
        resultR = num1 - num2;
        return resultR;
    }
    
    public int multi (int num1 , int num2){
        int resultM = 0;
        resultM = num1 * num2;
        return resultM;
    }
    
    public int div (int num1 , int num2){
        int resultD = 0;
        resultD = num1 / num2; 
        return resultD;
    }
    
}
